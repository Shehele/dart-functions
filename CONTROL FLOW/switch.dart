// Switch is the short form of if else
// it uses case and break
void main() {
  int age = 65;
  switch (age) {
    case 1:
      print("the value is 1");
      break;
    case 2:
      print("the value is 2");

      break;
    case 3:
      print("the value is 3");

    default:
      print("the age is not on the range");
  }
}
